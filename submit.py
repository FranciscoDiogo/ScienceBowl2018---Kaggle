#In some images RGB range is very compressed, 
def scale_img_canals(an_img):
    for i in range(IMG_CHANNELS):
        canal = an_img[:,:,i]
        canal = canal - canal.min()
        canalmax = canal.max()
        if canalmax > 0:
            factor = 255/canalmax
            canal = (canal * factor).astype(int)
        an_img[:,:,i] = canal

#tqdm prints a loop status (every 30 sec)
